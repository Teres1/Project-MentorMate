package myProject;

public class FinalEmployee {
	private String name;
	private double score;
	public FinalEmployee() {
		super();
	}
	public FinalEmployee(String name, double score) {
		super();
		this.name = name;
		this.score = score;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getScore() {
		return score;
	}
	public void setScore(double score) {
		this.score = score;
	}

}
